                                                <span style="font-size:14px"><span style="font-family:Arial,Helvetica,sans-serif"> Kedua uji/pertimbangan ini diatur dalam Pasal 2 ayat (4) UU KIP. Secara operasional, uji/pertimbangan konsekuensi diatur secara khusus di dalam Pasal 17 UU KIP.</span></span>
