                                                            <li style="text-align:justify"><span style="font-size:14px"><span style="font-family:Arial,Helvetica,sans-serif">Serta setelah dipertimbangkan dengan seksama bahwa menutup Informasi Publik dapat melindungi kepentingan yang lebih besar daripada membukanya atau secara teoritis dikenal dengan uji kepentingan public (balancing public interest test). </span></span></li>
