                                            <div class="accordion-body__contents">
                                                <span style="font-size:14px"><span style="font-family:Arial,Helvetica,sans-serif">Pertama, Daftar Informasi Publik adalah daftar yang wajib tersedia setiap saat di Badan Publik. Kedua, Daftar Informasi Publik dapat mempermudah petugas informasi dalam melayani permohonan Informasi Publik. Selain itu, Daftar Informasi Publik dapat digunakan untuk membantu penyusunan database Informasi Publik dan mengetahui Informasi Publik apa saja yang dikuasainya serta keberadaaan Informasi Publik tersebut berada di unit/satuan kerja mana, karena sering kali masing-masing unit/satuan kerja di dalam Badan Publik tidak mengetahui informasi apa yang berada di unit/satuan kerja lain.&nbsp;<br>
                                                        Ketiga, memudahkan masyarakat saat mencari informasi. Selain itu juga menginformasikan kepada publik mengenai informasi apa saja yang berada di Badan Publik.</span></span>
